/* ──────────────────────────────────────────────────────────────────────────
   Rich, deterministic seed for the Supplier Jobs surface. Used as the 42P01-
   safe fallback when the supplier_job_* tables aren't reachable yet, so every
   tab renders a believable on-site workflow during the V1 rollout.

   All times are relative to "now" so the demo never goes stale. Money is pence.
─────────────────────────────────────────────────────────────────────────── */

const HOUR = 3_600_000
const DAY = 86_400_000

function iso(offsetMs: number): string {
  return new Date(Date.now() + offsetMs).toISOString()
}
function dateOnly(offsetMs: number): string {
  return new Date(Date.now() + offsetMs).toISOString().slice(0, 10)
}

const EV_LABELS: Record<EvidenceItemKey, string> = {
  before_photos: "Before photos",
  after_photos: "After photos",
  completion_notes: "Completion notes",
  invoice: "Invoice",
  customer_signoff: "Customer sign-off",
}

function ev(received: Partial<Record<EvidenceItemKey, boolean>>): EvidenceItem[] {
  return (Object.keys(EV_LABELS) as EvidenceItemKey[]).map((key) => ({
    key,
    label: EV_LABELS[key],
    received: received[key] ?? false,
    required: true,
  }))
}

